// 生成更多模拟数据
export function generateMockConversations(count: number): Conversation[] {
  const topics = [
    'JavaScript 异步编程',
    'CSS Grid 布局',
    'Node.js 性能调优',
    'GraphQL API 设计',
    'Kubernetes 入门',
    'CI/CD 流水线搭建',
    '单元测试最佳实践',
    '设计模式应用',
    '算法与数据结构',
    '系统架构设计',
  ];

  const additional: Conversation[] = [];
  for (let i = 0; i < count; i++) {
    const date = new Date();
    date.setDate(date.getDate() - Math.floor(Math.random() * 60));
    const topic = topics[Math.floor(Math.random() * topics.length)];
    additional.push({
      uuid: `conv-${String(i + 11).padStart(3, '0')}`,
      name: `${topic} - 讨论 ${i + 1}`,
      created_at: date.toISOString(),
      updated_at: new Date(date.getTime() + Math.random() * 3600000).toISOString(),
      message_count: Math.floor(Math.random() * 20) + 3,
      total_tokens: Math.floor(Math.random() * 10000) + 2000,
      is_archived: Math.random() > 0.8,
      messages: [],
    });
  }
  return [...mockConversations, ...additional];
}
